(function () {
    var VALID = { compress: 1, 'images-to-pdf': 1, 'pdf-suite': 1, svg: 1, 'passport-studio': 1, 'heic-converter': 1, 'format-converter': 1, 'image-cropper': 1, 'collage-maker': 1, 'remove-bg': 1, 'photo-checker': 1, redactor: 1, 'ai-upscaler': 1 };

    function parseToolFromHash() {
        var h = (location.hash || '').replace(/^#/, '').trim();
        if (h === 'photo-studio') h = 'passport-studio';
        return VALID[h] ? h : 'compress';
    }

    function localePack() {
        var loc = window.__NEXUS_LOCALE || 'en';
        return window.__NEXUS_I18N_META?.[loc] || window.__NEXUS_I18N_META?.en || null;
    }

    function applyHeadMeta(tool) {
        var m = window.__NEXUS_TOOL_META;
        if (!m) return;
        var pack = localePack();
        var title = (pack && pack.titles && pack.titles[tool]) || m.titles[tool];
        if (title) document.title = title;
        var desc = (pack && pack.descriptions && pack.descriptions[tool]) || m.descriptions[tool];
        if (!desc) return;
        var metaDesc = document.querySelector('meta[name="description"]');
        if (metaDesc) metaDesc.setAttribute('content', desc);
        var ogDesc = document.querySelector('meta[property="og:description"]');
        if (ogDesc) ogDesc.setAttribute('content', desc);
        var twDesc = document.querySelector('meta[name="twitter:description"]');
        if (twDesc) twDesc.setAttribute('content', desc);
        if (title) {
            var ogTitle = document.querySelector('meta[property="og:title"]');
            if (ogTitle) ogTitle.setAttribute('content', title);
            var twTitle = document.querySelector('meta[name="twitter:title"]');
            if (twTitle) twTitle.setAttribute('content', title);
            var ogSite = document.querySelector('meta[property="og:site_name"]');
            if (ogSite) ogSite.setAttribute('content', 'NexusCompress');
        }
    }

    function applyToolState(tool) {
        var m = window.__NEXUS_TOOL_META;
        if (!m) return;
        var resolved = VALID[tool] ? tool : 'compress';

        document.querySelectorAll('[data-tool-panel]').forEach(function (panel) {
            var active = panel.dataset.toolPanel === resolved;
            panel.classList.toggle('is-hidden', !active);
            panel.setAttribute('aria-hidden', active ? 'false' : 'true');
            panel.tabIndex = active ? 0 : -1;
        });
        document.querySelectorAll('.tool-nav-link').forEach(function (link) {
            var active = link.dataset.tool === resolved;
            link.classList.toggle('active', active);
            link.setAttribute('aria-selected', active ? 'true' : 'false');
            link.tabIndex = active ? 0 : -1;
            if (active) link.setAttribute('aria-current', 'page');
            else link.removeAttribute('aria-current');
        });

        var tag = document.getElementById('site-logo-tag');
        var taglines = (localePack() && localePack().taglines) || m.taglines;
        if (tag && taglines[resolved]) tag.textContent = taglines[resolved];

        applyHeadMeta(resolved);

        function seoCopyFor(tool) {
            var loc = window.__NEXUS_LOCALE || 'en';
            if (loc === 'ar' && window.__NEXUS_I18N_SEO?.ar?.[tool]) {
                return window.__NEXUS_I18N_SEO.ar[tool];
            }
            return m.seo[tool] || m.seo.compress;
        }

        var copy = seoCopyFor(resolved);
        if (copy) {
            var set = function (id, text) {
                var el = document.getElementById(id);
                if (el && text != null) el.textContent = text;
            };
            if (copy.h1) set('seo-heading', copy.h1);
            set('seo-intro-title-1', copy.title1);
            set('seo-intro-1', copy.intro1);
            set('seo-intro-title-2', copy.title2);
            set('seo-intro-2', copy.intro2);
            set('seo-intro-title-3', copy.title3);
            set('seo-intro-3', copy.intro3);
            document.querySelectorAll('[data-seo-scope="compress"]').forEach(function (el) {
                el.classList.toggle('is-hidden', resolved !== 'compress');
            });
        }
        if (window.__NEXUS_APPLY_I18N) window.__NEXUS_APPLY_I18N();
    }

    window.__NEXUS_TOOL_SHELL = {
        parseTool: parseToolFromHash,
        applyHeadMeta: applyHeadMeta,
        applyToolState: applyToolState,
    };

    applyHeadMeta(parseToolFromHash());
})();
